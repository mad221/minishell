#include "minishell.h"

void	ft_search_exit(char *str, int *i)
{
	if ((str[*i] ==  ' ' && str[*i + 1] == 'e') || (*i == 0 && str[*i] == 'e'))
	{
		while (str[*i] == ' ')
			*i += 1;
		if (ft_strcmp(&str[*i], "exit") == 0)
			exit(EXIT_SUCCESS);
	}
}


